using System.Collections.Generic;

namespace Wheel.Development.Log4Me.Writers
{
    /// <summary>
    /// Representa el formato de salida de Log4Me.
    /// </summary>
    /// <remarks>
    ///     <para>
    ///         <h2 class="groupheader">Registro de versiones</h2>
    ///         <ul>
    ///             <li>1.0.0</li>
    ///             <table>
    ///                 <tr style="font-weight: bold;">
    ///                     <td>Autor</td>
    ///                     <td>Fecha</td>
    ///                     <td>Descripción</td>
    ///                 </tr>
    ///                 <tr>
    ///                     <td>Marcos Abraham Hernández Bravo.</td>
    ///                     <td>11/11/2016</td>
    ///                     <td>Versión Inicial.</td>
    ///                 </tr>
    ///             </table>
    ///         </ul>
    ///     </para>
    /// </remarks>
    internal class FormatoSalidaTO
    {
        /// <summary>
        /// Obtiene el formato de salida como una cadena de texto.
        /// </summary>
        /// <remarks>
        ///     <para>
        ///         <h2 class="groupheader">Registro de versiones</h2>
        ///         <ul>
        ///             <li>1.0.0</li>
        ///             <table>
        ///                 <tr style="font-weight: bold;">
        ///                     <td>Autor</td>
        ///                     <td>Fecha</td>
        ///                     <td>Descripción</td>
        ///                 </tr>
        ///                 <tr>
        ///                     <td>Marcos Abraham Hernández Bravo.</td>
        ///                     <td>11/11/2016</td>
        ///                     <td>Versión Inicial.</td>
        ///                 </tr>
        ///             </table>
        ///         </ul>
        ///     </para>
        /// </remarks>
        public string Formato { get; private set; }

        /// <summary>
        /// Obtiene el seárador del formato. 
        /// </summary>
        /// <remarks>
        ///     <para>
        ///         <h2 class="groupheader">Registro de versiones</h2>
        ///         <ul>
        ///             <li>1.0.0</li>
        ///             <table>
        ///                 <tr style="font-weight: bold;">
        ///                     <td>Autor</td>
        ///                     <td>Fecha</td>
        ///                     <td>Descripción</td>
        ///                 </tr>
        ///                 <tr>
        ///                     <td>Marcos Abraham Hernández Bravo.</td>
        ///                     <td>11/11/2016</td>
        ///                     <td>Versión Inicial.</td>
        ///                 </tr>
        ///             </table>
        ///         </ul>
        ///     </para>
        /// </remarks>
        public char Separador { get; private set; }

        /// <summary>
        /// Obtiene un diccionario de claves y formatos.
        /// </summary>
        /// <remarks>
        ///     <para>
        ///         <h2 class="groupheader">Registro de versiones</h2>
        ///         <ul>
        ///             <li>1.0.0</li>
        ///             <table>
        ///                 <tr style="font-weight: bold;">
        ///                     <td>Autor</td>
        ///                     <td>Fecha</td>
        ///                     <td>Descripción</td>
        ///                 </tr>
        ///                 <tr>
        ///                     <td>Marcos Abraham Hernández Bravo.</td>
        ///                     <td>11/11/2016</td>
        ///                     <td>Versión Inicial.</td>
        ///                 </tr>
        ///             </table>
        ///         </ul>
        ///     </para>
        /// </remarks>
        public IDictionary<string, string> TagFormato { get; private set; }

        /// <summary>
        /// Constructor que inicializa la instancia.
        /// </summary>
        /// <remarks>
        ///     <para>
        ///         <h2 class="groupheader">Registro de versiones</h2>
        ///         <ul>
        ///             <li>1.0.0</li>
        ///             <table>
        ///                 <tr style="font-weight: bold;">
        ///                     <td>Autor</td>
        ///                     <td>Fecha</td>
        ///                     <td>Descripción</td>
        ///                 </tr>
        ///                 <tr>
        ///                     <td>Marcos Abraham Hernández Bravo.</td>
        ///                     <td>11/11/2016</td>
        ///                     <td>Versión Inicial.</td>
        ///                 </tr>
        ///             </table>
        ///         </ul>
        ///     </para>
        /// </remarks>
        /// <param name="formato">Formato de salida.</param>
        /// <param name="separador">Separador del formato.</param>
        public FormatoSalidaTO(string formato, char separador)
        {
            Recargar(formato, separador);
        }

        /// <summary>
        /// Vuelve a analizar el formato en busca de los tags y sub-formatos.
        /// </summary>
        /// <remarks>
        ///     <para>
        ///         <h2 class="groupheader">Registro de versiones</h2>
        ///         <ul>
        ///             <li>1.0.0</li>
        ///             <table>
        ///                 <tr style="font-weight: bold;">
        ///                     <td>Autor</td>
        ///                     <td>Fecha</td>
        ///                     <td>Descripción</td>
        ///                 </tr>
        ///                 <tr>
        ///                     <td>Marcos Abraham Hernández Bravo.</td>
        ///                     <td>11/11/2016</td>
        ///                     <td>Versión Inicial.</td>
        ///                 </tr>
        ///             </table>
        ///         </ul>
        ///     </para>
        /// </remarks>
        public void Recargar()
        {
            TagFormato.Clear();
            ExtraerTagFormato();
        }

        /// <summary>
        /// Vuelve a analizar un formato y separador recibidos en busca de los tags y sub-formatos.
        /// </summary>
        /// <remarks>
        ///     <para>
        ///         <h2 class="groupheader">Registro de versiones</h2>
        ///         <ul>
        ///             <li>1.0.0</li>
        ///             <table>
        ///                 <tr style="font-weight: bold;">
        ///                     <td>Autor</td>
        ///                     <td>Fecha</td>
        ///                     <td>Descripción</td>
        ///                 </tr>
        ///                 <tr>
        ///                     <td>Marcos Abraham Hernández Bravo.</td>
        ///                     <td>11/11/2016</td>
        ///                     <td>Versión Inicial.</td>
        ///                 </tr>
        ///             </table>
        ///         </ul>
        ///     </para>
        /// </remarks>
        public void Recargar(string formato, char separador)
        {
            Formato = formato;
            Separador = separador;
            TagFormato = new Dictionary<string, string>();

            ExtraerTagFormato();
        }

        /// <summary>
        /// Extrae los tags y su correspondientes formatos, tal como el de la fecha.
        /// </summary>
        /// <remarks>
        ///     <para>
        ///         <h2 class="groupheader">Registro de versiones</h2>
        ///         <ul>
        ///             <li>1.0.0</li>
        ///             <table>
        ///                 <tr style="font-weight: bold;">
        ///                     <td>Autor</td>
        ///                     <td>Fecha</td>
        ///                     <td>Descripción</td>
        ///                 </tr>
        ///                 <tr>
        ///                     <td>Marcos Abraham Hernández Bravo.</td>
        ///                     <td>11/11/2016</td>
        ///                     <td>Versión Inicial.</td>
        ///                 </tr>
        ///             </table>
        ///         </ul>
        ///     </para>
        /// </remarks>
        private void ExtraerTagFormato()
        {
            string tag = string.Empty;
            string acumulador = string.Empty;

            bool comenzarAcumulacion = false;

            foreach (char caracter in Formato)
            {
                if (caracter == Separador && string.IsNullOrEmpty(tag) && string.IsNullOrEmpty(acumulador)) continue;

                if (caracter == '[') comenzarAcumulacion = true;

                if (comenzarAcumulacion && caracter != '[' && (!string.IsNullOrEmpty(tag) || caracter != ':') && caracter != ']')
                    acumulador += caracter;

                if (caracter == ':' && string.IsNullOrEmpty(tag))
                {
                    tag = string.Format("[{0}]", acumulador.Trim());
                    acumulador = string.Empty;
                }

                if (caracter == ']')
                {
                    if (string.IsNullOrEmpty(tag))
                    {
                        tag = string.Format("[{0}]", acumulador.Trim());
                        TagFormato.Add(tag, null);
                    }
                    else
                    {
                        TagFormato.Add(tag, acumulador.Trim());
                    }

                    tag = string.Empty;
                    acumulador = string.Empty;

                    comenzarAcumulacion = false;
                }
            }
        }
    }
}