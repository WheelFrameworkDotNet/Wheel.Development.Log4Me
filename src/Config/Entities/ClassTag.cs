        ///                     <td>Versión Inicial.</td>
        ///                 </tr>
        ///             </table>
        ///         </ul>
        ///     </para>
        /// </remarks>
        [XmlAttribute]
        public bool isPublic { get; set; }

        /// <summary>
        /// Permite obtener o establecer el valor que indica si se debe exigir que la clase haya sido declarada como privada.
