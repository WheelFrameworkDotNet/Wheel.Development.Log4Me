        /// Permite obtener o establecer el valor que indica si se debe exigir que la clase haya sido declarada como interna (de un ensamblado).
        /// </summary>
        /// <remarks>
        ///     <para>
        ///         <h2 class="groupheader">Registro de versiones</h2>
